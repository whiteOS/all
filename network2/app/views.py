def getUUID() -> str:
    import wmi
    mainboard = ""
    for board_id in wmi.WMI().Win32_BaseBoard():
        mainboard += board_id.SerialNumber
    return mainboard.replace("/", "")


def getIP() ->str:
    import socket
    try:
        hostname = socket.gethostname()
        ip = socket.gethostbyname(hostname)
        return ip
    except:
        return "无法获取IP地址！"


def getSSID() -> str:
    import os
    if "csust-yd" in os.popen('netsh WLAN show interfaces').read():
        return "csust-yd"
    else:
        return "当前WiFi非csust-yd"


def getPassword() -> str:
    import random
    import datetime
    now = datetime.datetime.now()
    random.seed(str(now.year)+str(now.month))
    str_list = list(getUUID())
    random.shuffle(str_list)
    return ''.join(str_list)


def isOnline() -> bool:
    import socket
    try:
        s = socket.socket()
        s.settimeout(0.3)
        status = s.connect_ex(('www.baidu.com', 443))
        s.close()
        if status == 0:
            return True
        else:
            return False
    except:
        return False


